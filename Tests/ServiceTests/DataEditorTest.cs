using System;
using System.Collections.Generic;
using System.Text;
using NUnit.Framework;
using TicketingSystem.Services;
using TicketingSystem.Models;
using System.Linq;

namespace Tests.ServiceTests
{
    [TestFixture]
    class DataEditorTest
    {
        [Test]
        public void PostEditTest()
        {
            using (var context = new TicketingSystemDBContext())
            {
                JobType j = new JobType();
                j.JobName = "Miscellaneous";

                TicketData td = new TicketData()
                {
                    EntryId = context.TicketData.Where(t => t.TicketWorker.FullName == "System Admin").FirstOrDefault().EntryId,
                    TripNum = 111,
                    StageNum = "S01",
                    JobType = j,
                    EntryAuthor = context.Users.Where(u => u.FullName == "System Admin").FirstOrDefault(),
                    TicketWorker = context.Users.Where(w => w.FullName == "System Admin").FirstOrDefault(),
                    WorkerName = "System Admin",
                    StartTime = DateTime.Now,
                    Comments = "Edit generated by DataEditor.PostEdit unit test"
                };

                DataEditor DaEd = new DataEditor();
                UserData ud = Auth0APIClient.GetUserData("admin123@gmail.com");
                Assert.IsTrue(DaEd.PostEditor(td, ud));
            }
        }
    }
}
